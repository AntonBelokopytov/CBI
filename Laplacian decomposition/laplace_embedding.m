function [L, D, W_n, W] = laplace_embedding(Feat, sigma, N_neigb)
    % ВХОДНЫЕ ДАННЫЕ:
    % Feat    - матрица признаков [n_epochs x n_features]
    % sigma   - параметр ширины ядра теплопроводности
    % N_neigb - количество ближайших соседей (k)
    
    n_epochs = size(Feat, 1);
    
    % 1. Поиск ближайших соседей
    % Ищем N_neigb + 1, так как первой найденной точкой всегда будет сама точка
    [idx, D_knn] = knnsearch(Feat, Feat, 'K', N_neigb + 1, 'Distance', 'euclidean');
    
    % Удаляем саму точку из списка (расстояние до себя = 0)
    idx(:, 1) = []; 
    D_knn(:, 1) = []; 
    
    % 2. Вычисление весов только для локальных соседей
    weights = exp(-(D_knn.^2) / (2 * sigma^2));
    
    % 3. Построение разреженной матрицы (sparse)
    % Создаем индексы строк для каждой найденной связи
    row_idx = repmat((1:n_epochs)', 1, N_neigb);
    
    % Собираем матрицу W_n. Она будет занимать минимум оперативной памяти
    W_n = sparse(row_idx(:), idx(:), weights(:), n_epochs, n_epochs);
    
    % 4. Симметризация графа
    % В k-NN графах связь часто делают симметричной: если точка A считает B 
    % своим соседом, то связь устанавливается в обе стороны.
    % max(W_n, W_n') - стандартный и быстрый способ для таких задач.
    W_n = max(W_n, W_n'); 
    
    % 5. Вычисление Лапласиана
    % Используем spdiags для создания разреженной диагональной матрицы
    D = spdiags(sum(W_n, 2), 0, n_epochs, n_epochs); 
    L = D - W_n;
    
    % 6. Возврат полной матрицы W
    % Поскольку суть оптимизации - избавиться от плотной матрицы N x N, 
    % возвращаем пустой массив, чтобы не сломать сигнатуру функции, 
    % либо можете вернуть саму W_n, если старый код где-то жестко требует W.
    W = []; 
end